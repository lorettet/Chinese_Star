/**
 * Tray's plinth of Chinese Checker
 * */
public class Plinth {

	/**
	 * the value of the plinth
	 * a non-existent plinth is equal to -1
	 * an empty plinth is equal to 0
	 * an filled plinth is equal to the player number
	 * */
	private int value;
	
	
	public Plinth(int value){
		
		this.value = value;
	}
	
	
	
}
